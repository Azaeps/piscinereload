int		ft_iterative_factorial(int nb)
{
	int x;

	x = nb;
	if (x == 0 || x == 1)
		return (1);
	if (x < 0 || x > 12)
		return (0);
	while (x > 1)
	{
		nb = nb * (x - 1);
		x--;
	}
	return (nb);
}
